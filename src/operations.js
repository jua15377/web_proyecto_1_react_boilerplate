function sum(a, b) {
    return a + b;
  }
  export  {sum}
function sub(a, b) {
    return a - b;
  }
  export {sub}

function mult(a, b) {
    return a * b;
  }
  export{mult}
function div(a, b) {
    return Math.round (a / b);
  }
  export {div}

function modulo(a, b) {
    return a % b;
  }
  export {modulo}

function equality(a, b) {
    if (a===b){
      return 1
    }
    else{
      return 0
    }
  }
  export {equality}